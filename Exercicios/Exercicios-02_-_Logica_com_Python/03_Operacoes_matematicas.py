#!/usr/bin/python
# -*- coding: utf-8 -*-

num1 = int(input("Digite o primeiro número para as operações: "))   # Lê o primero número
num2 = int(input("Digite o segundo número para as operações: "))   # Lê o segundo número

soma = num1 + num2  # Soma
sub = num1 - num2     # Subtração
produto = num1 * num2   # Multiplicação
divisao = num1 / num2   # Divisão

# Imprime o resultado das operações
print("Resultado da soma:",soma)
print("Resultado da subtração:", sub)
print("Resultado da multiplicação:", produto)
print("Resultado da divisão:", divisao)
